#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		i;
	int		s1_len;
	char	*result;

	s1_len = 0;
	while (s1[s1_len])
		s1_len++;
	result = (char *)malloc(sizeof(char) * (s1_len + 1));
	if (!result)
		return (0);
	i = 0;
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
