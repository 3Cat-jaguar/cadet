#include <stddef.h>

void	*ft_memset(void *str, int c, size_t len)
{
	size_t	i;
	char	*s;

	i = 0;
	s = str;
	while (i < len)
	{
		*(s + i) = c;
		i++;
	}
	return ((void *)str);
}
