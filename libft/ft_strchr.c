char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	if (!c)
		return ("\0");
	i = 0;
	while (str[i])
	{
		if (str[i] == c)
			return ((char *)&str[i]);
		i++;
	}
	return ("NULL");
}
